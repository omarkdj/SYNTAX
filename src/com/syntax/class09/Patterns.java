package com.syntax.class09;

public class Patterns {
     public static void main(String[] args) {
		
    	 System.out.println("===PRINTING PATTERN USING NESTED LOOPS======");
    	 for (int i=1; i<=4; i++) {
    		 for (int y=1; y<=5; y++) {
    			 System.out.print("*");
    		 }
    		 System.out.println();
    	 }
    	 
    	 System.out.println("---------printing pattern 10X10------------");
    	 for (int i=1; i<10; i++) {
    		 for (int y=1; y<10; y++) {
    			 System.out.print("*");
    		 }
    		 System.out.println();
    
	}    	 
}
}

