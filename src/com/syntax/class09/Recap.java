package com.syntax.class09;

public class Recap {

	public static void main(String[] args) {
      
		for (int i = 1; i <= 3; i++ ) {
			 for (int j = 1; j <= 3; j++) {
				 System.out.println(i +" " + j);
			 }
			 System.out.println("----------------------");
			 // using nested loop to print 10 to 99
			 
			 for ( int x=1; x<=9; x++) {
				 for (int y=0; y<=9; y++) {
					 System.out.println(x +""+ y);
				 }
			 }
		}
	}

}
